package com.kbu.java.example.ch02;

public class StringExample {
    public static void main(String[] args){

        String str = "String is referece type";
        System.out.println(str);

        str = "String is \n referece type";
        System.out.println(str);   

        str = "String is \t referece type";
        System.out.println(str);   

        str = "String is \"referece type\"";
        System.out.println(str); 
    }
}