public class TrueFalseTable {
    public static void main(String[] args){
        
        System.out.println("true & true 는" + (true & true));
        System.out.println("true & false 는" + (true & false));
        System.out.println("false & true 는" + (false & true));
        System.out.println("false & false 는" + (true & false));        
        
        System.out.println("true | true 는" + (true | true));        
        System.out.println("true | false 는" + (true | false));
        System.out.println("false | true 는" + (false | true));
        System.out.println("false | false 는" + (true | false));
    }
}