public class LiteralExample {
    public static void main(String[] args) {
        int a = 12;	// int는 데이터 타입,  a는 identifier, 12는 리터럴
        // 12 or 0X0C or 014 or 0B1100
        // 12l or 12L	// long
        // 123.4f or 123.4F     // float
        // 123.4d or 123.4D     // double

        // 5e2 // 5.0 * 10^2
        // 0.12E-2 // 0.12 * 10^-2

            
        float floatPi = 3.14; // compile error
        double doublePi = 3.14;
    }

}
