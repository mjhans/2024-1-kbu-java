public class VariableDeclare {
    public static void main(){

        int         age;        // 정수(int)를 저장할수 있는 age 변수 선언
        double      values;     // 실수(double)를 저장할수 있는 values 변수 선언

        age         = 10;       // age 변수에 정수 10 저장
        values      = 20.0;     // values 변수에 실수 20.0 저장

        System.out.printf("%s, %s",age, values);
        
        // int x;
        // int y;
        // int z; 

        //int x, y, z;

        int x = 0, y =3, z = 6; // 선언하면서 값 저장
        
        System.out.println(x + ", " + y + ", " + z);
    

    }
}
